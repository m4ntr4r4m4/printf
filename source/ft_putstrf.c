#include "ft_printf.h"

int	ft_putstrf(char *s)
{
   int i;

   i = 0;
   if (s == NULL)
       return (write(1, "(null)", 6));
   while (s[i] != '\0')
   {
       (void)write(1, &s[i], 1);
       i++;
   }
   return (i);
}
